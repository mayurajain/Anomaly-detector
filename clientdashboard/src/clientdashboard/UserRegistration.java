    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
        
        String uName = userName.getText();
        String pWord = password.getText();
        String rPWord = confirmPassword.getText();
        String pnumber = phoneNumber.getText();
        String eid = emailId.getText();
        FileInputStream fisR = new FileInputStream("register.txt");
        byte bbR[] = new byte[fisR.available()];
        fisR.read(bbR);
        fisR.close();
        FileInputStream fisU = new FileInputStream("user.txt");
        byte bbU[] = new byte[fisU.available()];
        fisU.read(bbU);
        fisU.close();
        if(!pWord.equals(rPWord)){
            JOptionPane.showMessageDialog(null,"both password and reenter passwords must be same");
        }
        
        else{
        if(bbR.length==0){
          String  regString = uName+":"+pWord+":"+rPWord+":"+pnumber+":"+eid;
          String uString = uName+":"+pWord;
          FileOutputStream fosR = new FileOutputStream("register.txt");
          fosR.write(regString.getBytes());
          fosR.close();
          FileOutputStream fosU = new FileOutputStream("user.txt");
          fosU.write(uString.getBytes());
          fosU.close();
          JOptionPane.showMessageDialog(null,"success in regstrtion");
        }
        else{
          String  regString = uName+":"+pWord+":"+rPWord+":"+pnumber+":"+eid;
          String uString = uName+":"+pWord;
          String actRegString = new String(bbR)+"\r\n"+regString;
          String actuString = new String(bbU)+"\r\n"+uString;
          FileOutputStream fosR = new FileOutputStream("register.txt");
          fosR.write(actRegString.getBytes());
          fosR.close();
          FileOutputStream fosU = new FileOutputStream("user.txt");
          fosU.write(actuString.getBytes());
          fosU.close();
          JOptionPane.showMessageDialog(null,"success in regstrtion");
        }
        }
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

